#Assignment operator

i = 1

# "+=" operation equivalent to i = i +5
i += 5
print(i)

# "-=" operation equivalent to i = i -2
i -= 2
print(i)

# "*=" operation equivalent to i = i * (i+3)
i *= (i+3)
print(i)

# "/=" operation equivalent to i = i /2
i /= 2
print(i)

# "%=" operation equivalent to i = i %3
i %= (3)
print(i)

# output
# 6
# 4
# 28
# 14.0
# 2.0