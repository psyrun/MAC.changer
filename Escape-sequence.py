#Escape sequence 

#New line character
print("Hello earth ! \nHello world !\n") 

#Tab space
print("Hello earth ! \tHello world !\n")

#Back space
print("Hello earth ! \bHello world ! \n\n")


#Printing Escape sequence with \\ double slash 

print("printing double \\\\ ")
print("printing mountain /\\/\\/\\/\\")
print("printing \\t tab escape character")
print("\\ \" \\n \\t \\ \' \n\n")



print(r"printing double \\   \n")
print(r"printing mountain /\/\/\/\ \n")
print(r"printing \t tab escape character")
print(r"\ \" \n \t \ \'")

